//Data structures are the fundammental building blocks of computer programming. They define how data is organised, stored, and manipulated within a program

//What is Data Structure?
//A data structure is a storge that is used to store and organize data. It is a way of arranging data on a computer so that it can be accessed and updated efficiently
//It is also used for processing, retrieving , and storing data

//Classification of Data Structure
//1. Linear Data Structure: Data structure in which data elements are arrangd sequentially or linearly, where each element is attached to its previous and next adjecent elements
//Example: Array, Stack, Queue, Linked List, etc.
//2. Static Data Structure: has fixed memory size.
//Example: Array
//3. Dynamic Data Stucture: the size is not fixed, it can be randomly udated during the runtime
//Example: Queue, Stack, etc.
//4. Non-Linear Data Structure: Data structures where data elements are not placed sequentially or linearly. All the elements can't be traversed in a single run only
//Examples: Trees and Graphs

//1. Array
//Array is a linear data structure that stores a collection of elements of the same data type. Elements are allocated contiguous memory, allowing for cnstant-time access. Each element has a unique index number

//2. Matrix/Grid
//matrix is a two-dimensional array of elements, arranged in rows and columns. It is represented as a rectangular grid, with each element at the intersection of a row and column

//3. String 
//String is a sequence of characters, typically used to represent text. Allows the manipulation and processing of textual data in computer programs

//4. Stack
//Stack is a linear data structure that follows the Last In, First Out (LIFO) principle

//5. Queue
//Queue is a linear data structure that follows the First In, First Out (FIFO) priciple

//6. Linked List
//Linkd list i a linear data structure that stores data in nodes, which are connected by pointers. Nodes are not stored in contiguous memory locations and can only be accessed sequentially, starting from the head of list

//7. Hash
//Hashing is a technique that generates a fixed-size output (hash value) from an input of variable size using mathematical formulas called hash functions

//8. Tree
//Tree is a non-linear, hierarchical data structure consisting of nodes connected by edges, with a top node called the root and nodes having child nodes

//9. Binary Tree
//Binary Tree is a non-linear and hierarchical data structure where each node has at most two children referred to as the left child and the right child

//10. Binary Search Tree
//Binary Search Tree is a type of binary tree in which each node's left subtree contains only values smaller than the node, and each node's right subtree contains only values greater than the node. This roperty applies recursively, meaning that for every node, its left and righ subtrees must also satisfy the conditions of a valid Binary Search Tree

//11. Heap
//Heap is a complete binary tree data struture that satisfies the heap property

//12. Graph
//Graph is a non-linear data structure consisting of a finite set of vertices(or nodes) and a set of edges(or links) that connect a pair of nodes

//13. Advanced Data Structures
//Advanced Data Structures are complex arrangement of data which are used to organise, store, and manipulate data efficiently, enabling faster and more effective processing in complex algorithms. Some of them are: Segment Trees, Trie, Binary Indexed Tree, Suffix Array etc.